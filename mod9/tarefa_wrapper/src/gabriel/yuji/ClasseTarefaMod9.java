package gabriel.yuji;

public class ClasseTarefaMod9 {

	public static void main(String[] args) {
		// Primitive
		int n1 = 10;
		System.out.println(n1);
		
		// Casting
		short n2 = (short) n1;
		System.out.println(n2);
		
		// Boxing (Wrapper)
		Integer n3 = Integer.valueOf(n1);
		System.out.println(n3);

	}

}
